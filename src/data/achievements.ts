export const getAchievementsByType = (type: 'user' | 'couple'): Achievement[] => {
  return ACHIEVEMENTS.filter(a => a.type === type);
};

export const getAchievementsByTier = (tier: Achievement['tier']): Achievement[] => {
  return ACHIEVEMENTS.filter(a => a.tier === tier);
};

export const getTotalPossiblePoints = (type?: 'user' | 'couple'): number => {
  const filtered = type ? ACHIEVEMENTS.filter(a => a.type === type) : ACHIEVEMENTS;
  return filtered.reduce((sum, a) => sum + a.points, 0);
};
